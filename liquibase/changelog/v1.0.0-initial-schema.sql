CREATE TABLE findings (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    scan_id UUID NOT NULL,

    -- Scanner information
    scan_type TEXT NOT NULL CHECK (scan_type IN ('sast', 'sca', 'secrets', 'license')),
    tool_name TEXT NOT NULL,
    tool_version TEXT,

    -- Finding details
    title TEXT NOT NULL,
    description TEXT,
    severity TEXT NOT NULL CHECK (severity IN ('critical', 'high', 'medium', 'low', 'info')),

    -- Location in code
    file_path TEXT,
    start_line INT,
    end_line INT,
    start_column INT,
    end_column INT,
    code_snippet TEXT,

    -- Vulnerability details (SAST/SCA)
    rule_id TEXT,
    cwe_id TEXT,
    cve_id TEXT,
    cvss_score DECIMAL(3,1),
    cvss_vector TEXT,

    -- Dependency information (SCA)
    package_name TEXT,
    package_version TEXT,
    fixed_version TEXT,

    -- License information
    license_name TEXT,
    license_type TEXT,

    -- Remediation
    remediation TEXT,
    "references" TEXT[],

    -- Metadata
    raw_output JSONB,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_findings_scan ON findings(scan_id);
CREATE INDEX idx_findings_severity ON findings(severity, scan_id);
CREATE INDEX idx_findings_type ON findings(scan_type, scan_id);
CREATE INDEX idx_findings_cve ON findings(cve_id) WHERE cve_id IS NOT NULL;

-- =============================================================================
-- Audit Log
-- =============================================================================
CREATE TABLE audit_logs (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    organization_id UUID NOT NULL REFERENCES organizations(id) ON DELETE CASCADE,
    user_id UUID NOT NULL,
    action TEXT NOT NULL,
    resource_type TEXT NOT NULL,
    resource_id UUID,
    metadata JSONB,
    ip_address INET,
    user_agent TEXT,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_audit_logs_org ON audit_logs(organization_id, created_at DESC);
CREATE INDEX idx_audit_logs_user ON audit_logs(user_id, created_at DESC);
CREATE INDEX idx_audit_logs_resource ON audit_logs(resource_type, resource_id);

